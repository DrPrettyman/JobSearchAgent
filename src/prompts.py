AI_WRITING_GUIDELINES = """AVOID THESE AI WRITING PATTERNS:

1. Contrastive reframes: Never use "It wasn't just X, it was Y" or "This isn't about X, it's about Y". Just state what it is directly.

2. Negation for false depth: Avoid "more than just", "not only... but also", "not simply about". Make the point without the negation setup.

3. Rule of three: Don't use three parallel items for rhetorical effect ("expertise, passion, and commitment"). Use two items or four, or just one.

4. Paragraph-opening hedges: Never start with "When it comes to...", "In today's rapidly evolving...", "In the realm of...". Start with the actual subject.

5. Flattering intensifiers: Avoid "fascinating", "captivating", "remarkable", "compelling", "truly", "deeply", "genuinely". Don't call anything a "journey" or "transformation". Let facts speak.

6. Excessive transitions: Don't use "Furthermore", "Moreover", "Indeed", "In summary". Don't start multiple sentences with "This" referring back. If ideas connect, the connection should be obvious.

7. Mirrored structure: Don't make every paragraph the same length or follow the same pattern. A two-sentence paragraph followed by a longer one feels human.

8. Em-dashes: Use colons, commas, or periods instead of em-dashes (--).

9. Generic phrases: Never use "I am writing to apply", "aligns closely with", "I would welcome the opportunity", "I am excited to".

10. Hook lines with dramatic colons: Don't use "The problem was simple:" or "Here's the thing:" to create artificial tension. Write "The problem was that..." or just state the point directly. Never structure sentences for dramatic reveal.

11. LinkedIn buzzwords: Never use "at the intersection of", "in the X space", "leverage", "at scale", "ecosystem", "synergy", "thought leadership". These are content-marketing language, not human speech.

12. Empty intensifiers for experience: Don't say experience "runs deep", skills are "extensive", or background is "strong". These add no information. Either give specifics or just state the fact plainly ("I've built APIs" not "My API experience runs deep").

13. Generic closings: Don't end with vague enthusiasm like "I'd like to help build whatever comes next" or "exactly where I want to be". End with something specific about what you want to do or contribute, or just stop after your last substantive point.

14. Performative tone: Write as if speaking to one person, not posting content for an audience. If a sentence would work as a LinkedIn post, rewrite it.

15. Indirect phrasing: Don't say "X is something I care about" or "Y is something I've always valued". Just state it directly: "I care about X" or better, show it through what you've done.

16. Formulaic paragraph starts: Don't start every paragraph with "I" or follow the same subject-verb pattern. Vary how paragraphs begin: start with the work, the company, a specific detail, or a short sentence that isn't "I [verb]"."""
